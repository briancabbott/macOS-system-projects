CCCryptorStatus
CCDesIsWeakKey(const void *key, size_t length)
{
    size_t n;
    
    if (length != 8)
        return -1;
    
    for (n = 0; n < sizeof(weak_keys) / sizeof(weak_keys[0]); n++)
        if (memcmp(weak_keys[n], key, 8) == 0)
            return -1;
    
    return 0;
}

